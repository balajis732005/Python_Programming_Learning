#To get multiple inputs by using SPLIT() FUNCTION
name1,name2,name3=input("Enter 3 names : ").split()
print("NAME 1 : ",name1)
print("NAME 2 : ",name2)
print("NAME 3 : ",name3)
place1,place2,place3=input("Enter 3 places which has split by comma(,) : ").split(",")
print("PLACE 1 : ",place1)
print("PLACE 2 : ",place2)
print("PLACE 3 : ",place3)
