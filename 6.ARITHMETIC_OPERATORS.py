#ARITHMETIC_OPERATORS
# + , - , * , / , // , % , **
a=int(input("Enter a number 1 : "))
b=int(input("Enter a number 2 : "))
print("ADDITION : ",a+b)
print("SUBTRACTION : ",a-b)
print("MULTIPLICATION : ",a*b)
#The return value of division is always "float"
print("DIVISION : ",a/b)
#If we want to display the integer after divison we can use "floor division"
print("FLOOR DIVISION : ",a//b)
print("MODULUS : ",a%b)
print("EXPONENT : ",a**b)
